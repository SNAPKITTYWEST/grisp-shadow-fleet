#!/usr/bin/env node
/**
 * RESURRECT — Shadow Orchestrator · RANSOM.WORM delivery agent
 * Points at any GitHub repo, crawls it, runs all 4 agents, then
 * opens a PR with the WORM resurrection receipt embedded.
 *
 * Run: node agents/resurrect.mjs --repo <github-url-or-local-path> [--dry-run]
 */
import { execSync, spawnSync } from 'node:child_process'
import { mkdirSync, existsSync, rmSync, writeFileSync } from 'node:fs'
import { join, basename, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'
import { sealEvent, getHead } from './worm-chain.mjs'

const __dir = dirname(fileURLToPath(import.meta.url))
const WORK = join(__dir, '..', '.resurrect-workspace')

function run(cmd, cwd, opts = {}) {
  try {
    return execSync(cmd, { cwd, encoding: 'utf8', stdio: opts.silent ? 'pipe' : 'inherit', ...opts })
  } catch (e) {
    if (!opts.optional) throw e
    return ''
  }
}

function log(tag, msg) {
  const ts = new Date().toISOString().slice(11, 19)
  console.log(`[${ts}] [${tag}] ${msg}`)
}

async function cloneRepo(url, dest) {
  log('WORM', `Cloning target: ${url}`)
  if (existsSync(dest)) rmSync(dest, { recursive: true, force: true })
  run(`git clone --depth 1 ${url} ${dest}`)
  log('WORM', 'Clone complete')
}

async function auditRepo(repoPath) {
  log('AUDIT', 'Running metric stream...')
  const { run: runMetrics } = await import('./metric-stream.mjs')
  const metrics = await runMetrics({
    repoPath,
    outPath: join(repoPath, '.ransom-worm-metrics.json'),
  })
  return metrics
}

async function translateRepo(repoPath, outDir) {
  log('BIFROST', 'Scanning for translatable sources...')
  const { run: runBifrost } = await import('./bifrost-translator.mjs')
  const { readdirSync, statSync } = await import('node:fs')
  const { extname } = await import('node:path')

  const TRANSLATE_EXTS = new Set(['.rs', '.ts', '.hs', '.lean', '.py', '.mjs'])
  const IGNORE = new Set(['node_modules', '.git', 'dist', 'target', '.next'])

  function findSources(dir, files = []) {
    let entries
    try { entries = readdirSync(dir) } catch { return files }
    for (const e of entries) {
      if (IGNORE.has(e)) continue
      const full = join(dir, e)
      try {
        if (statSync(full).isDirectory()) findSources(full, files)
        else if (TRANSLATE_EXTS.has(extname(e))) files.push(full)
      } catch {}
    }
    return files
  }

  const sources = findSources(repoPath).slice(0, 5) // cap at 5 for demo
  log('BIFROST', `Found ${sources.length} sources (capped at 5)`)
  mkdirSync(outDir, { recursive: true })

  const results = []
  for (const src of sources) {
    log('BIFROST', `Translating: ${src.replace(repoPath, '')}`)
    const result = await runBifrost({ sourcePath: src, targets: ['rust', 'lean4'], outDir }).catch(() => null)
    if (result) results.push(result)
  }
  return results
}

async function injectWatermark(outDir) {
  log('WATERMARK', 'Injecting sovereign fingerprints...')
  const { run: runWatermark } = await import('./watermark.mjs')
  return runWatermark({ target: outDir })
}

