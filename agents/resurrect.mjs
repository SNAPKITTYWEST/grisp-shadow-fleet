async function sendWorm(targetUrl, opts = {}) {
  const repoName = basename(targetUrl.replace(/\.git$/, ''))
  const isGitHub = targetUrl.startsWith('https://github.com') || targetUrl.startsWith('git@github.com')
  const isLocal = existsSync(targetUrl)

  log('WORM', `=== RANSOM.WORM INITIATED ===`)
  log('WORM', `Target: ${targetUrl}`)
  log('WORM', `Dry run: ${opts.dryRun ? 'YES' : 'NO'}`)

  // Step 1: Get the repo
  let repoPath
  if (isLocal) {
    repoPath = targetUrl
    log('WORM', 'Using local path')
  } else {
    repoPath = join(WORK, repoName)
    await cloneRepo(targetUrl, repoPath)
  }

  // Step 2: Audit
  const metrics = await auditRepo(repoPath).catch(e => { log('AUDIT', 'Error: ' + e.message); return null })

  // Step 3: Translate
  const outDir = join(repoPath, '.ransom-worm-output')
  await translateRepo(repoPath, outDir).catch(e => log('BIFROST', 'Error: ' + e.message))

  // Step 4: Watermark
  await injectWatermark(outDir).catch(e => log('WATERMARK', 'Error: ' + e.message))

  // Step 5: Seal everything to WORM chain
  const { seal: wormSeal } = sealEvent('RESURRECT', 'resurrection-complete', {
    target: targetUrl,
    repoName,
    totalLines: metrics?.summary?.totalLines ?? 0,
    totalFiles: metrics?.summary?.totalFiles ?? 0,
  })

  log('WORM', `Resurrection seal: ${wormSeal}`)

  if (opts.dryRun) {
    log('WORM', '=== DRY RUN — no PR created ===')
    const pr = buildReceiptPR(repoName, metrics, wormSeal, wormSeal)
    console.log('\n--- PR PREVIEW ---')
    console.log('TITLE:', pr.title)
    console.log('BODY:\n', pr.body)
    return { wormSeal, metrics, dryRun: true }
  }

  // Step 6: Open PR (GitHub only)
  if (isGitHub) {
    log('WORM', 'Forking and creating resurrection PR...')
    try {
      // Fork
      const forkOut = run(`gh repo fork ${targetUrl} --clone=false 2>&1`, __dir, { optional: true, silent: true })
      log('WORM', 'Fork: ' + (forkOut?.trim()?.slice(0, 60) || 'done'))

      // Create branch in cloned copy
      const branch = `ransom-worm/${wormSeal.slice(0, 12)}`
      run(`git checkout -b ${branch}`, repoPath)

      // Write receipt file
      writeFileSync(join(repoPath, 'RANSOM_WORM_RECEIPT.md'), buildReceiptPR(repoName, metrics, wormSeal, wormSeal).body)
      run(`git add RANSOM_WORM_RECEIPT.md`, repoPath)
      if (existsSync(join(repoPath, '.ransom-worm-output'))) {
        run(`git add .ransom-worm-output/ 2>/dev/null || true`, repoPath, { optional: true })
      }
      run(`git commit -m "RANSOM.WORM: Resurrection receipt — ${wormSeal.slice(0, 16)}"`, repoPath)

      // Push to fork
      run(`git push origin ${branch}`, repoPath)

      // Open PR
      const pr = buildReceiptPR(repoName, metrics, wormSeal, wormSeal)
      const prResult = run(`gh pr create --title "${pr.title}" --body "${pr.body.replace(/"/g, "'")}" --head ${branch}`, repoPath, { silent: true, optional: true })
      log('WORM', 'PR: ' + (prResult?.trim() || 'check GitHub'))
    } catch (e) {
      log('WORM', 'PR failed: ' + e.message)
    }
  }

  log('WORM', `=== RESURRECTION COMPLETE — ${wormSeal} ===`)
  return { wormSeal, metrics }
}

// CLI
const args = process.argv.slice(2)
const repoIdx = args.indexOf('--repo')
const dryRun = args.includes('--dry-run')

if (repoIdx < 0) {
  console.error('Usage: node agents/resurrect.mjs --repo <github-url-or-local-path> [--dry-run]')
  console.error('Examples:')
  console.error('  node agents/resurrect.mjs --repo https://github.com/owner/dead-repo --dry-run')
  console.error('  node agents/resurrect.mjs --repo /path/to/local/repo')
  process.exit(1)
}

sendWorm(args[repoIdx + 1], { dryRun }).catch(console.error)
